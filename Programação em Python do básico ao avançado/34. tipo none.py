"""
Tipo None

O tipo de dado None em python representa o tipo sem tipo, ou poderia ser conhecido também como tipo vazio, porém, falar que é um tipo é mais apropriado.

OBS: O tipo None é SEMPRE especificado com a primeira letra maiuscula

Certo: None
Errado: none

- Podemos utilizar None quando queremos criar uma variável e inicializá-la com um tipo sem tipo, antes de recebemos um valor final.

OBS: O tipo None em Python é sempre considerado como falso.
"""

numeros = None
print(numeros)
print(type(numeros))
